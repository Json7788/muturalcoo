public class HeartRatePoint {

    private int day;
    private int value;

    public HeartRatePoint(int day, int value) {
        this.day = day;
        this.value = value;
    }

    public int getDay() {
        return day;
    }

    public void setDay(int day) {
        this.day = day;
    }

    public int getValue() {
        return value;
    }

    public void setValue(int value) {
        this.value = value;
    }

    @Override
    public String toString() {
        return "HeartRatePoint{" +
                "day=" + day +
                ", value=" + value +
                '}';
    }
}
